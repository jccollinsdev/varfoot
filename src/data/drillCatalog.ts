export type DrillCategory = (typeof DRILL_CATEGORIES)[number];

/**
 * count        -> numeric count input, optionally framed as "X of Y" via maxAttempts
 * timed        -> Start / Stop / Reset stopwatch producing a duration in seconds
 * timed_count  -> a fixed countdown/count-up timer (timerSeconds) plus a count input
 *                 logged when it ends (e.g. "25 passes in 1 minute")
 * checkin      -> a simple daily numeric log (calories, water, sleep hours, recovery rating)
 */
export type DrillInputType = "count" | "timed" | "timed_count" | "checkin";

export type LoadType = "light" | "moderate" | "heavy" | "recovery";

export type DrillSource = "pdf" | "physical-assessment" | "catalog";

export type Drill = {
  id: string;
  name: string;
  category: DrillCategory;
  skillTags: string[];
  equipment: string[];
  imagePath: string;
  instructions: string;
  setup: string;
  action: string;
  measurement: string;
  inputType: DrillInputType;
  scoreDirection: ScoreDirection;
  freshmanTarget: number;
  jvTarget: number;
  varsityTarget: number;
  unit: string;
  estimatedMinutes: number;
  muscleGroups: string[];
  loadType: LoadType;
  restSeconds: number;
  planUseCase: string;
  requiresPartner: boolean;
  safetyNotes?: string;
  /** For "X of Y" count drills (e.g. 18 of 20 stops). */
  maxAttempts?: number;
  /** For timed_count drills — the fixed countdown length in seconds. */
  timerSeconds?: number;
  source: DrillSource;
};

